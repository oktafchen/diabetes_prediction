# Input sliders
pregnancies = st.slider("ğŸ¤° Jumlah Kehamilan", 0, 15, 0, help="Jumlah kehamilan yang pernah dialami")
glucose = st.slider("ğŸ¬ Glukosa (mg/dL)", 50, 200, 100, help="Normal: 70â€“140 mg/dL")
blood_pressure = st.slider("ğŸ«€ Tekanan Darah (mmHg)", 40, 140, 80, help="Normal: 80â€“120 mmHg")
skin_thickness = st.slider("ğŸ“ Ketebalan Lipatan Kulit (mm)", 0, 100, 20, help="Biasanya 10â€“50 mm")
insulin = st.slider("ğŸ’‰ Insulin (mu U/ml)", 0, 900, 80, help="Normal: 16â€“166 mu U/ml")
bmi = st.slider("âš–ï¸ BMI", 10.0, 60.0, 25.0, 0.1, help="Normal: 18.5â€“24.9")
