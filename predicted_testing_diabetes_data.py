age = st.slider("ğŸ‚ Usia", 10, 100, 30, help="Usia pasien (tahun)")

# Prediksi
if st.button("ğŸ” Prediksi"):
    # Masukkan data ke array
    input_data = np.array([[pregnancies, glucose, blood_pressure, skin_thickness, insulin, bmi, dpf, age]])
    
    # Scaling
    input_scaled = scaler.transform(input_data)
    
    # Prediksi
    prediction = model.predict(input_scaled)
    prob = model.predict_proba(input_scaled)[0][1]  # Probabilitas diabetes
    
    # Speedometer Chart
    fig = go.Figure(go.Indicator(
        mode = "gauge+number",
        value = prob * 100,
        title = {'text': "Probabilitas Diabetes (%)"},
        gauge = {
            'axis': {'range': [0, 100]},
            'bar': {'color': "darkblue"},
            'steps': [
                {'range': [0, 40], 'color': "lightgreen"},
                {'range': [40, 70], 'color': "yellow"},
                {'range': [70, 100], 'color': "red"}
            ],
        }
    ))
    
    st.plotly_chart(fig)
    
    # Teks hasil
    if prediction[0] == 1:
        st.error(f"âš ï¸ Hasil: Kemungkinan **TERDIAGNOSA** diabetes ({prob*100:.2f}%)")
    else:
        st.success(f"âœ… Hasil: Kemungkinan **TIDAK TERDIAGNOSA** diabetes ({(1-prob)*100:.2f}%)")

